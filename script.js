const navToggle = document.querySelector("[data-nav-toggle]");
const navMenu = document.querySelector("[data-nav-menu]");
const header = document.querySelector("[data-header]");
const year = document.querySelector("[data-year]");
const profileImage = document.querySelector("[data-profile-image]");
const languageButtons = document.querySelectorAll("[data-lang]");
const projectTabs = document.querySelectorAll("[data-filter]");
const projectCards = document.querySelectorAll("[data-category]");
const revealItems = document.querySelectorAll(".reveal");
const supportedLanguages = Object.keys(translations);
const languageStorageKey = "portfolio-language";
let currentLanguage = "no";

const getTranslation = (key) => translations[currentLanguage]?.[key] || translations.no[key] || "";

const updateNavToggleLabel = () => {
  if (!navToggle) return;
  const isOpen = navToggle.getAttribute("aria-expanded") === "true";
  const label = getTranslation(isOpen ? "nav_toggle_close" : "nav_toggle_open");
  navToggle.setAttribute("aria-label", label);
  const srLabel = navToggle.querySelector(".sr-only");
  if (srLabel) srLabel.textContent = label;
};

const applyLanguage = (language) => {
  currentLanguage = supportedLanguages.includes(language) ? language : "no";
  document.documentElement.lang = currentLanguage;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    const value = getTranslation(key);
    if (value) element.textContent = value;
  });

  document.querySelectorAll("[data-i18n-attr]").forEach((element) => {
    element.dataset.i18nAttr.split(",").forEach((pair) => {
      const [attribute, key] = pair.split(":").map((part) => part.trim());
      const value = getTranslation(key);
      if (attribute && value) element.setAttribute(attribute, value);
    });
  });

  languageButtons.forEach((button) => {
    const isActive = button.dataset.lang === currentLanguage;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  localStorage.setItem(languageStorageKey, currentLanguage);
  updateNavToggleLabel();
};

const closeMobileNav = () => {
  if (!navToggle || !navMenu) return;
  navToggle.setAttribute("aria-expanded", "false");
  navMenu.classList.remove("is-open");
  document.body.classList.remove("nav-open");
  updateNavToggleLabel();
};

if (year) {
  year.textContent = new Date().getFullYear();
}

if (profileImage) {
  const markProfileLoaded = () => {
    profileImage.classList.add("is-loaded");
    profileImage.classList.remove("is-missing");
  };

  const markProfileMissing = () => {
    profileImage.classList.add("is-missing");
    profileImage.classList.remove("is-loaded");
  };

  if (profileImage.complete) {
    if (profileImage.naturalWidth > 0) {
      markProfileLoaded();
    } else {
      markProfileMissing();
    }
  }

  profileImage.addEventListener("load", markProfileLoaded);
  profileImage.addEventListener("error", markProfileMissing);
}

if (navToggle && navMenu) {
  navToggle.addEventListener("click", () => {
    const isOpen = navToggle.getAttribute("aria-expanded") === "true";
    navToggle.setAttribute("aria-expanded", String(!isOpen));
    navMenu.classList.toggle("is-open", !isOpen);
    document.body.classList.toggle("nav-open", !isOpen);
    updateNavToggleLabel();
  });

  navMenu.querySelectorAll("a").forEach((link) => {
    link.addEventListener("click", closeMobileNav);
  });
}

languageButtons.forEach((button) => {
  button.addEventListener("click", () => {
    applyLanguage(button.dataset.lang);
  });
});

projectTabs.forEach((tab) => {
  tab.addEventListener("click", () => {
    const filter = tab.dataset.filter;

    projectTabs.forEach((button) => {
      const isActive = button === tab;
      button.classList.toggle("is-active", isActive);
      button.setAttribute("aria-pressed", String(isActive));
    });

    projectCards.forEach((card) => {
      const categories = card.dataset.category.split(" ");
      const shouldShow = filter === "all" || categories.includes(filter);

      if (shouldShow) {
        card.hidden = false;
        requestAnimationFrame(() => card.classList.remove("is-hiding"));
      } else {
        card.classList.add("is-hiding");
        window.setTimeout(() => {
          if (card.classList.contains("is-hiding")) card.hidden = true;
        }, 180);
      }
    });
  });
});

if (!window.matchMedia("(prefers-reduced-motion: reduce)").matches) {
  document.body.classList.add("motion-ready");
}

if ("IntersectionObserver" in window) {
  revealItems.forEach((item, index) => {
    item.style.setProperty("--reveal-delay", `${Math.min(index % 4, 3) * 70}ms`);
  });

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
          observer.unobserve(entry.target);
        }
      });
    },
    { rootMargin: "0px 0px -8% 0px", threshold: 0.12 }
  );

  revealItems.forEach((item) => observer.observe(item));
} else {
  revealItems.forEach((item) => item.classList.add("is-visible"));
}

window.addEventListener("scroll", () => {
  if (!header) return;
  header.classList.toggle("is-scrolled", window.scrollY > 8);
});

applyLanguage(localStorage.getItem(languageStorageKey) || "no");
